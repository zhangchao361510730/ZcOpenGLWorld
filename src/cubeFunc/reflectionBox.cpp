bool reflectionBox::InitReflectionBox() {
    // cube VAO
    loadVertices();
    glGenVertexArrays(1, &cubeVAO);
    glGenBuffers(1, &cubeVBO);
    glBindVertexArray(cubeVAO);
    glBindBuffer(GL_ARRAY_BUFFER, cubeVBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(cubeVertices), &cubeVertices, GL_STATIC_DRAW);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(1);
    glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)(3 * sizeof(float)));

    std::string SkyBoxPath_fs = std::string(CMAKE_CURRENT_DIR).append("/glslFile/reflectionBox.fs");
    std::string SkyBoxPath_vs = std::string(CMAKE_CURRENT_DIR).append("/glslFile/reflectionBox.vs");
    shaderReflectionBox_ = new ShaderGLSLTool(SkyBoxPath_vs.c_str(),SkyBoxPath_fs.c_str());
    shaderReflectionBox_->use();
    shaderReflectionBox_->setInt("skybox", 0);
    return true;
}

